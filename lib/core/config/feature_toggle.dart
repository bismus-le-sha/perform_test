/// Feature toggles для переключения между оптимизированными и неоптимизированными подходами.
/// Используется в учебных целях для демонстрации влияния различных техник оптимизации.
enum FeatureToggle {
  /// Использовать compute() для вычисления чисел Фибоначчи (изоляция от UI потока)
  optimFibonacci,
  
  /// Правильное обновление состояния для кнопки прокрутки вверх
  correctDataUpdate,
  
  /// Использовать ListView.builder вместо Column для ленивой загрузки
  lazyLoad,
  
  /// Загружать уменьшенные изображения вместо полных
  optimImageSize,
  
  /// Минимизировать дорогие операции рендеринга
  minimizeExpensiveRendering,
  
  /// Использовать compute() для парсинга большого JSON
  largeJsonParce,
}


